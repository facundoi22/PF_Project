-- -----------------------------------------------------
-- Table EQUIPOS
-- -----------------------------------------------------
CREATE TABLE IF NOT EXISTS EQUIPOS (
  EQUIPO_ID INT NOT NULL AUTO_INCREMENT,
  NOMBRE VARCHAR(60) NOT NULL,
  CAPITAN_ID VARCHAR(45) NOT NULL,
  ACTIVO VARCHAR(1) NOT NULL DEFAULT 'Y',
