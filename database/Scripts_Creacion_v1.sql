-- -----------------------------------------------------
-- Table CANCHAS
-- -----------------------------------------------------
CREATE TABLE IF NOT EXISTS CANCHAS (
  SEDE_ID INT NOT NULL,
  CANCHA_ID INT NOT NULL,
  DESCRIPCION VARCHAR(45) NOT NULL,
  DEPORTE_ID INT NOT NULL,
  PRECIO INT NOT NULL,
