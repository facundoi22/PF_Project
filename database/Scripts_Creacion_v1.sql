-- -----------------------------------------------------
-- Table FASES
-- -----------------------------------------------------
CREATE TABLE IF NOT EXISTS FASES (
  TORNEO_ID INT NOT NULL,
  FASE_ID INT NOT NULL,
  DESCRIPCION VARCHAR(45) NOT NULL,
  PRIMARY KEY (TORNEO_ID, FASE_ID),
