-- -----------------------------------------------------
-- Table PARTIDOS
-- -----------------------------------------------------
CREATE TABLE IF NOT EXISTS PARTIDOS (
  TORNEO_ID INT NOT NULL,
  FASE_ID INT NOT NULL,
  PARTIDO_ID INT NOT NULL,
  LOCAL_ID INT NOT NULL,
  VISITA_ID INT NOT NULL,
  FECHA DATE NULL,
  HORA TIME(5) NULL,
  ARBITRO_ID VARCHAR(45) NULL,
  PUNTOS_LOCAL INT(3) NULL,
  PUNTOS_VISITA INT(3) NULL,
  LOCAL_OK VARCHAR(1) NULL,
  VISITA_OK VARCHAR(1) NULL,
  SEDE_ID INT NOT NULL,
  CANCHA_ID INT NOT NULL,
  PRIMARY KEY (TORNEO_ID, FASE_ID, PARTIDO_ID),
  INDEX fk_PARTIDOS_EQUIPOS1_idx (LOCAL_ID ASC),
  INDEX fk_PARTIDOS_EQUIPOS2_idx (VISITA_ID ASC),
  INDEX fk_PARTIDOS_USUARIOS1_idx (ARBITRO_ID ASC),
  INDEX fk_PARTIDOS_CANCHAS1_idx (SEDE_ID ASC, CANCHA_ID ASC),
  CONSTRAINT fk_PARTIDOS_FECHAS1
    FOREIGN KEY (TORNEO_ID , FASE_ID)
    REFERENCES FASES (TORNEO_ID , FASE_ID)
    ON DELETE RESTRICT ON UPDATE CASCADE,
  CONSTRAINT fk_PARTIDOS_EQUIPOS1
    FOREIGN KEY (LOCAL_ID)
    REFERENCES EQUIPOS (EQUIPO_ID)
    ON DELETE RESTRICT ON UPDATE CASCADE,
  CONSTRAINT fk_PARTIDOS_EQUIPOS2
    FOREIGN KEY (VISITA_ID)
    REFERENCES EQUIPOS (EQUIPO_ID)
    ON DELETE RESTRICT ON UPDATE CASCADE,
  CONSTRAINT fk_PARTIDOS_USUARIOS1
    FOREIGN KEY (ARBITRO_ID)
    REFERENCES USUARIOS (USUARIO_ID)
    ON DELETE RESTRICT ON UPDATE CASCADE,
  CONSTRAINT fk_PARTIDOS_CANCHAS1
    FOREIGN KEY (SEDE_ID , CANCHA_ID)
    REFERENCES CANCHAS (SEDE_ID , CANCHA_ID)
    ON DELETE RESTRICT ON UPDATE CASCADE)
;


-- -----------------------------------------------------
-- Table MENSAJES
-- -----------------------------------------------------
CREATE TABLE IF NOT EXISTS MENSAJES (
  MENSAJE_ID INT NOT NULL AUTO_INCREMENT,
  MENSAJE LONGBLOB NOT NULL,
  EMISOR_ID VARCHAR(45) NOT NULL,
  RECEPTOR_ID VARCHAR(45) NULL,
  PARTIDOS_TORNEO_ID INT NULL,
  PARTIDOS_FECHA_ID INT NULL,
  PARTIDOS_PARTIDO_ID INT NULL,
  MENSAJE_ORIGINAL_ID INT NULL,
  PRIMARY KEY (MENSAJE_ID),
  INDEX fk_MENSAJES_USUARIOS1_idx (EMISOR_ID ASC),
  INDEX fk_MENSAJES_USUARIOS2_idx (RECEPTOR_ID ASC),
  INDEX fk_MENSAJES_PARTIDOS1_idx (PARTIDOS_TORNEO_ID ASC, PARTIDOS_FECHA_ID ASC, PARTIDOS_PARTIDO_ID ASC),
  INDEX fk_MENSAJES_MENSAJES1_idx (MENSAJE_ORIGINAL_ID ASC),
  CONSTRAINT fk_MENSAJES_USUARIOS1
    FOREIGN KEY (EMISOR_ID)
    REFERENCES USUARIOS (USUARIO_ID)
    ON DELETE RESTRICT ON UPDATE CASCADE,
  CONSTRAINT fk_MENSAJES_USUARIOS2
    FOREIGN KEY (RECEPTOR_ID)
    REFERENCES USUARIOS (USUARIO_ID)
    ON DELETE RESTRICT
    ON UPDATE CASCADE,
  CONSTRAINT fk_MENSAJES_PARTIDOS1
    FOREIGN KEY (PARTIDOS_TORNEO_ID , PARTIDOS_FECHA_ID , PARTIDOS_PARTIDO_ID)
