DROP DATABASE IF EXISTS PROXIMAFECHA;
CREATE DATABASE  PROXIMAFECHA;
USE PROXIMAFECHA;

-- -----------------------------------------------------
-- Table DEPORTES
-- -----------------------------------------------------
CREATE TABLE IF NOT EXISTS DEPORTES (
  DEPORTE_ID INT NOT NULL AUTO_INCREMENT,
  DESCRIPCION VARCHAR(45) NULL,
  MAX_JUGADORES INT(2) NULL,
  PRIMARY KEY (DEPORTE_ID))
;


-- -----------------------------------------------------
-- Table TIPOS_TORNEO
-- -----------------------------------------------------
CREATE TABLE IF NOT EXISTS TIPOS_TORNEO (
  TIPO_TORNEO_ID VARCHAR(1) NOT NULL,
  DESCRIPCION VARCHAR(45) NOT NULL,
  PRIMARY KEY (TIPO_TORNEO_ID))
;


-- -----------------------------------------------------
-- Table PAISES
-- -----------------------------------------------------
CREATE TABLE IF NOT EXISTS PAISES (
  PAIS_ID VARCHAR(3) NOT NULL,
  PAIS VARCHAR(45) NOT NULL,
  ISO VARCHAR(2) NOT NULL,
  PRIMARY KEY (PAIS_ID))
;


-- -----------------------------------------------------
-- Table PROVINCIAS
-- -----------------------------------------------------
CREATE TABLE IF NOT EXISTS PROVINCIAS (
  PAIS_ID VARCHAR(3) NOT NULL,
  PROVINCIA_ID VARCHAR(3) NOT NULL,
  PROVINCIA VARCHAR(45) NOT NULL,
  PRIMARY KEY (PAIS_ID, PROVINCIA_ID),
  CONSTRAINT fk_PROVINCIAS_PAISES1
    FOREIGN KEY (PAIS_ID)
    REFERENCES PAISES (PAIS_ID)
    ON DELETE RESTRICT ON UPDATE CASCADE)
;


-- -----------------------------------------------------
-- Table SEDES
-- -----------------------------------------------------
CREATE TABLE IF NOT EXISTS SEDES (
  SEDE_ID INT NOT NULL AUTO_INCREMENT,
  NOMBRE VARCHAR(45) NOT NULL,
  PAIS_ID VARCHAR(3) NOT NULL,
  PROVINCIA_ID VARCHAR(3) NOT NULL,
  CODIGO_POSTAL VARCHAR(10) NOT NULL,
  CALLE VARCHAR(45) NOT NULL,
  ALTURA INT(5) NOT NULL,
  TELEFONO VARCHAR(20)  NOT NULL,
  DETALLES VARCHAR(200) NULL,
  PRIMARY KEY (SEDE_ID),
  INDEX fk_SEDES_PROVINCIAS1_idx (PAIS_ID ASC, PROVINCIA_ID ASC),
  CONSTRAINT fk_SEDES_PROVINCIAS1
    FOREIGN KEY (PAIS_ID , PROVINCIA_ID)
    REFERENCES PROVINCIAS (PAIS_ID , PROVINCIA_ID)
    ON DELETE RESTRICT ON UPDATE CASCADE)
;


-- -----------------------------------------------------
-- Table TORNEOS
-- -----------------------------------------------------
CREATE TABLE IF NOT EXISTS TORNEOS (
  TORNEO_ID INT NOT NULL AUTO_INCREMENT,
  NOMBRE VARCHAR(60) NOT NULL,
  DEPORTE_ID INT NOT NULL,
  TIPO_TORNEO_ID VARCHAR(1) NOT NULL,
  CANTIDAD_EQUIPOS INT(2) NOT NULL,
  FECHA_INICIO DATE NULL,
  SEDE_ID INT NULL,
  PRIMARY KEY (TORNEO_ID),
  INDEX fk_TORNEOS_TIPOS_TORNEO1_idx (DEPORTE_ID ASC),
  INDEX fk_TORNEOS_TIPOS_TORNEO2_idx (TIPO_TORNEO_ID ASC),
  INDEX fk_TORNEOS_SEDES1_idx (SEDE_ID ASC),
  CONSTRAINT fk_TORNEOS_TIPOS_TORNEO1
    FOREIGN KEY (DEPORTE_ID)
    REFERENCES DEPORTES (DEPORTE_ID)
    ON DELETE RESTRICT
    ON UPDATE CASCADE,
  CONSTRAINT fk_TORNEOS_TIPOS_TORNEO2
    FOREIGN KEY (TIPO_TORNEO_ID)
    REFERENCES TIPOS_TORNEO (TIPO_TORNEO_ID)
    ON DELETE RESTRICT ON UPDATE CASCADE,
  CONSTRAINT fk_TORNEOS_SEDES1
    FOREIGN KEY (SEDE_ID)
    REFERENCES SEDES (SEDE_ID)
    ON DELETE RESTRICT ON UPDATE CASCADE)
;


-- -----------------------------------------------------
-- Table USUARIOS
-- -----------------------------------------------------
CREATE TABLE IF NOT EXISTS USUARIOS (
  USUARIO_ID VARCHAR(45) NOT NULL,
  PASSWORD VARCHAR(256) NOT NULL,
  NOMBRE VARCHAR(45) NOT NULL,
  APELLIDO VARCHAR(45) NOT NULL,
